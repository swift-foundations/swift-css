import CSS_Standard
import HTML_Renderable

extension CSS {
    @discardableResult
    public func flex(
        _ flex: W3C_CSS_Flexbox.Flex?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: HTML.Selector? = nil,
        pseudo: HTML.Pseudo? = nil
    ) -> CSS<some HTML.View> {
        CSS<HTML.Styled<Base>>(base: base.inlineStyle(flex, media: media, selector: selector, pseudo: pseudo))
    }
}
