import CSS_Standard
import HTML_Renderable

extension CSS {
    @discardableResult
    public func fontWeight(
        _ fontWeight: W3C_CSS_Fonts.FontWeight?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: HTML.Selector? = nil,
        pseudo: HTML.Pseudo? = nil
    ) -> CSS<HTML.AnyView> {
        CSS<HTML.AnyView>(base: HTML.AnyView(base.inlineStyle(fontWeight, media: media, selector: selector, pseudo: pseudo)))
    }
}
