import CSS_Standard
import HTML_Renderable

extension CSS {
    @discardableResult
    public func floodColor(
        _ floodColor: W3C_CSS_Color.FloodColor?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: HTML.Selector? = nil,
        pseudo: HTML.Pseudo? = nil
    ) -> CSS<HTML.AnyView> {
        CSS<HTML.AnyView>(base: HTML.AnyView(base.inlineStyle(floodColor, media: media, selector: selector, pseudo: pseudo)))
    }
}
